#include	<stdlib.h>

#include	"my.h"

int	my_putstr(char *str)
{
  int	i;

  i = -1;
  if (!str)
    return (EXIT_FAILURE);
  while (str[++i])
    my_putchar(str[i]);
  return (EXIT_SUCCESS);
}
