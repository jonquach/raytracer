#include	<stdlib.h>

#include	"global.h"
#include	"color.h"

int	cpy_color(t_color *old, t_color *new)
{
  if (old == NULL || new == NULL)
    return (EXIT_FAILURE);
  new->red = old->red;
  new->green = old->green;
  new->blue = old->blue;
  return (EXIT_SUCCESS);
}
